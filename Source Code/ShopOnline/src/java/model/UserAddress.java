package model;

/**
 *
 * @author Administrator
 */
public class UserAddress {
    private String uaID,userID,pname,dname,wname,prname,strname,detailAddress,phone,email;

    public UserAddress() {
    }

    public UserAddress(String uaID, String userID, String pname, String dname, String wname, String prname, String strname, String detailAddress, String phone, String email) {
        this.uaID = uaID;
        this.userID = userID;
        this.pname = pname;
        this.dname = dname;
        this.wname = wname;
        this.prname = prname;
        this.strname = strname;
        this.detailAddress = detailAddress;
        this.phone = phone;
        this.email = email;
    }

    public String getUaID() {
        return uaID;
    }

    public void setUaID(String uaID) {
        this.uaID = uaID;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getWname() {
        return wname;
    }

    public void setWname(String wname) {
        this.wname = wname;
    }

    public String getPrname() {
        return prname;
    }

    public void setPrname(String prname) {
        this.prname = prname;
    }

    public String getStrname() {
        return strname;
    }

    public void setStrname(String strname) {
        this.strname = strname;
    }

    public String getDetailAddress() {
        return detailAddress;
    }

    public void setDetailAddress(String detailAddress) {
        this.detailAddress = detailAddress;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}
